"""
4、将字典{'x': 'A', 'y': 'B', 'z': 'C' } 通过推导式转换为：['x=A','y=B','z=C']
"""
dict_01 = {'x': 'A', 'y': 'B', 'z': 'C'}
list_01 = [f'{key}={value}' for key, value in dict_01.items()]
print(list_01)
# ['x=A', 'y=B', 'z=C']
